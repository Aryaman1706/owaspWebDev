// * array.findIndex() // -> returns the index of first element that passed filter function || -1

// * array.includes() // -> returns true if element is present in array

// * array.map() // -> returns a new transformed array

// * array.slice() // -> returns a copy of array from start (1st arg) to end (2nd arg)

// * array.sort() // -> sort an array according to compare function hence mutates the array

// * array.reverse() // -> reverse the order of array hence mutates the array

